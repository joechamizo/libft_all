#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t count)
{
	unsigned char		*d;
	const unsigned char	*s;

	if (!dest && !src)
		return (NULL);
	d = (unsigned char *)dest;
	s = (const unsigned char *)src;
	if (d < s)
		while (count--)
			*d++ = *s++;
	else
	{
		d += count;
		s += count;
		while (count--)
			*--d = *--s;
	}
	return (dest);
}
